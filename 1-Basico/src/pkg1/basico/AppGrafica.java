public class AppGrafica {
    public AppGrafica(){
    
    }
    private double suma(double x,double y){
        double suma = x + y;
        return suma;
    }
    private double resta(double x, double y){
        double resta = x - y;
        return resta;
    }
    private double mult(double x, double y){
        double mult = x * y;
        return mult;
    }
    private double div(double x,double y){
        double mult = x / y;
        return mult;
    }
}
